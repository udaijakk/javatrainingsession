package JavaBasics;

public class LoopsConcept1 {
	
	public static void main(String[] args) {
		
		int i=1;
		while (i<=10) {
		System.out.println(i);
		i=i+1;
	}
		System.out.println("###########################");
		
		int j=0;
		while(j<=5) {
			System.out.println(j);
			j=j+1;
		}
		
		System.out.println("##########################");
		
		for(int k=10; k>=1; k--) {
			System.out.println(k);
		}
	}
}
